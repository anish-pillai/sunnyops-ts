    const received = [
      b.hra_received, b.sd_received, b.gst_received, 
      b.others_received, b.amount_credited
    ].reduce((s: number, v: any) => s + parseFloat(v?.toString() || "0"), 0);
    
    return Math.max(0, base - deductions - received);
