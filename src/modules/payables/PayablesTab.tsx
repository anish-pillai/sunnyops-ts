  const bb = banks.length > 0 
    ? banks.reduce((s, b) => s + (b.isOD ? Math.min(0, parseFloat((b.balance as any) || "0")) : parseFloat((b.balance as any) || "0")), 0) 
    : parseFloat(bankBalance || "0");
  const odUsed = banks.filter(b => b.isOD).reduce((s, b) => s + Math.max(0, parseFloat((b.balance as any) || "0")), 0);
